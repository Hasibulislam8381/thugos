    /**
     * Store a newly created resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $carts = Cart::where('user_id', Auth::user()->id)
            ->get();

        if ($carts->isEmpty()) {
            flash(translate('Your cart is empty'))->warning();
            return redirect()->route('home');
        }

        $address_id = Session::get('addressData');
        if (isset($address_id)) {
            $address = Address::where('id', $address_id)->first();
        } else {
            $address = Address::where('user_id', Auth::user()->id)->first();
        }

        $shippingAddress = [];
        if ($address != null) {
            $shippingAddress['name']        = $address->name ?? Auth::user()->name;
            $shippingAddress['email']       = $address->email ?? Auth::user()->email;
            $shippingAddress['address']     = $address->address;
            $shippingAddress['country']     = $address->country->name ?? null;
            $shippingAddress['city_id']       = $address->city_id ?? null;
            $shippingAddress['zone_id']        = $address->zone_id ?? null;
            $shippingAddress['area_id']        = $address->area_id ?? null;
            $shippingAddress['postal_code'] = $address->postal_code ?? null;
            $shippingAddress['phone']       = $address->phone;
            if ($address->latitude || $address->longitude) {
                $shippingAddress['lat_lang'] = $address->latitude . ',' . $address->longitude;
            }
        }

        $combined_order = new CombinedOrder;
        $combined_order->user_id = Auth::user()->id;
        $combined_order->shipping_address = json_encode($shippingAddress);
        $combined_order->save();

        $seller_products = array();


        $coupon_amount = Session::get('coupon');
        foreach ($carts as $cartItem) {
            $product_ids = array();
            $product = Product::find($cartItem['product_id']);
            if (isset($seller_products[$product->user_id])) {
                $product_ids = $seller_products[$product->user_id];
            }
            array_push($product_ids, $cartItem);
            $seller_products[$product->user_id] = $product_ids;
            $coupon_discount = $cartItem->discount;
        }

        foreach ($seller_products as $seller_product) {
            $order = new Order;
            $order->combined_order_id = $combined_order->id;
            $order->user_id = Auth::user()->id;
            $order->shipping_address = $combined_order->shipping_address;

            $order->payment_type = $request->payment_option;
            $order->delivery_viewed = '0';
            $order->payment_status_viewed = '0';
            $order->code = date('Ymd-His') . rand(10, 99);
            $order->date = strtotime('now');
            $order->save();

            $subtotal = 0;
            $tax = 0;
            $shipping = Session::get('delivery_charge');
            // $coupon_discount = 0;

            $coupon_code = Coupon::where('discount', $coupon_amount)->first();
            $offer_discount = 0;


            //Order Details Storing
            foreach ($seller_product as $cartItem) {
                $product = Product::find($cartItem['product_id']);

                $subtotal += $cartItem['price'] * $cartItem['quantity'];
                $tax += $cartItem['tax'] * $cartItem['quantity'];


                $product_variation = $cartItem['variation'];

                $product_stock = $product->stocks->where('variant', $product_variation)->first();
                if ($product->digital != 1 && $cartItem['quantity'] > $product_stock->qty) {
                    flash(translate('The requested quantity is not available for ') . $product->getTranslation('name'))->warning();
                    $order->delete();
                    return redirect()->route('cart')->send();
                } elseif ($product->digital != 1) {
                    $product_stock->qty -= $cartItem['quantity'];
                    $product_stock->save();
                }

                $order_detail = new OrderDetail;
                $order_detail->order_id = $order->id;
                $order_detail->seller_id = $product->user_id;
                $order_detail->product_id = $product->id;
                $order_detail->variation = $product_variation;
                $order_detail->price = $cartItem['price'] * $cartItem['quantity'];
                $order_detail->tax = $cartItem['tax'] * $cartItem['quantity'];
                $order_detail->shipping_type = $cartItem['shipping_type'];
                $order_detail->product_referral_code = $cartItem['product_referral_code'];
                $order_detail->shipping_cost = Session::get('delivery_charge');

                if ($cartItem['shipping_type'] == 'pickup_point') {
                    $order_detail->pickup_point_id = $cartItem['pickup_point'];
                }
                //End of storing shipping cost

                $order_detail->quantity = $cartItem['quantity'];
                $order_detail->save();
                $offer_discount += getOfferDiscount($order_detail->product_id, $order_detail->quantity);

                $product->num_of_sale += $cartItem['quantity'];
                $product->save();

                $order->seller_id = $product->user_id;

                if ($product->added_by == 'seller' && $product->user->seller != null) {
                    $seller = $product->user->seller;
                    $seller->num_of_sale += $cartItem['quantity'];
                    $seller->save();
                }

                if (addon_is_activated('affiliate_system')) {
                    if ($order_detail->product_referral_code) {
                        $referred_by_user = User::where('referral_code', $order_detail->product_referral_code)->first();

                        $affiliateController = new AffiliateController;
                        $affiliateController->processAffiliateStats($referred_by_user->id, 0, $order_detail->quantity, 0, 0);
                    }
                }
            }



            $order->grand_total = $subtotal + $tax + $shipping - $offer_discount;


            // if ($seller_product[0]->coupon_code != null) {
            //     // if (Session::has('club_point')) {
            //     //     $order->club_point = Session::get('club_point');
            //     // }
            //     $order->coupon_discount = $coupon_discount;
            //     $order->grand_total -= $coupon_discount;

            //     $coupon_usage = new CouponUsage;
            //     $coupon_usage->user_id = Auth::user()->id;
            //     $coupon_usage->coupon_id = Coupon::where('code', $seller_product[0]->coupon_code)->first()->id;
            //     $coupon_usage->save();
            // }


            if (Session::has('delivery_charge')) {
                $order->shipping_cost = Session::get('delivery_charge');
                $order->save();
            }
            if (Session::has('coupon')) {
                // if (Session::has('club_point')) {
                //     $order->club_point = Session::get('club_point');
                // }
                $order->coupon_discount = $coupon_discount;
                $order->grand_total -= $coupon_discount;

                $coupon_usage = new CouponUsage;
                $coupon_usage->user_id = Auth::user()->id;
                $coupon_usage->coupon_id = Coupon::where('code', $coupon_code->code)->first()->id;
                $coupon_usage->save();
            }

            $combined_order->grand_total += $order->grand_total;

            $order->save();

            // $sitename = env('APP_NAME');

            // $email = get_setting('contact_email');

            // $from_email = $request->email;
            // $shipping_addressfor_mail = json_decode($order->shipping_address);
            // $city_name = getCityName($shipping_addressfor_mail->city_id);
            // $zone_name = getZoneName($shipping_addressfor_mail->zone_id, $shipping_addressfor_mail->city_id);
            // $area_name = getAreaName($shipping_addressfor_mail->area_id, $shipping_addressfor_mail->zone_id);

            // $customer_address = "$city_name, $zone_name, $area_name";

            // $data = array('name' => $shipping_addressfor_mail->name, 'email' => $shipping_addressfor_mail->email, 'phone' => $shipping_addressfor_mail->phone, 'address' => $customer_address);
            // Mail::send('orderMail', $data, function ($message) use ($sitename, $email, $from_email) {
            //     $message->to(env('MAIL_USERNAME'), $sitename)->subject('New Order to Mou Gallery');
            //     $message->from('admin@gmail.com', $sitename);
            // });
        }



        $combined_order->save();

        $request->session()->put('combined_order_id', $combined_order->id);
    }

