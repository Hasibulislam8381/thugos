    public function bulk_send_pathao(Request $request)
    {
        $store_id = $request->store_id;
        $message = "";
        $messageShown = false; // Flag to track whether message has been shown

        if ($request->id) {
            foreach ($request->id as $order_id) {
                $order_data = Order::where('id', $order_id)->first();
                $consignment_id = pathao_add_order($store_id, $order_data);
                $order_data->tracking_code = $consignment_id;
                $order_data->save();
                $set_count = strlen($consignment_id);
                if ($set_count > 14 && !$messageShown) {
                    $message .= "Something Wrong";
                    $messageShown = true; // Set the flag to true
                } elseif (!$messageShown) {
                    $message .= "Your order has been sent";
                    $messageShown = true; // Set the flag to true
                }
            }
        }

        return $message;
    }

    public function bulk_send_steadfast(Request $request)
    {
        $store_id = $request->store_id;
        $message = "";
        $messageShown = false;

        if ($request->id) {
            $order_ids = $request->id;
            $orders = Order::whereIn('id', $order_ids)->get();

            foreach ($orders as $order_data) {
                if (is_null($order_data->tracking_code)) {
                    $shipping_address_json = $order_data->shipping_address;
                    $shipping_address = json_decode($shipping_address_json, true);
                    $name = $shipping_address['name'];
                    $phone = $shipping_address['phone'];
                    $address = $shipping_address['address'];

                    $grand_total = $order_data->grand_total;
                    $invoice_id = $order_data->id;

                    $consignment_id = sendtosteadfast($name, $phone, $address, $grand_total, $invoice_id);

                    $order_data->tracking_code = $consignment_id;
                    $order_data->save();
                    $set_count = strlen($consignment_id);
                    if ($set_count > 24) {
                        $message .= "Something went wrong with order ID {$order_data->id}. ";
                    } else {
                        $message .= "Order has been sent to Steadfast ";
                    }
                } else {
                    $message .= "Order already has a tracking code. ";
                }
            }
        }

        return $message;
    }


